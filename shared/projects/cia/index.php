                    <h2>Companhia</h2>

                    <div class="row">

                        <div class="col-sm-12 col-lg-6">
                            <p>A <b>Cia de Atores Invisíveis</b> foi criada em 2004, no Rio de Janeiro, por <b>Márcio Moreira</b> e <b>Katia Jorgensen</b>. A inspiração maior foi o livro "<i>O Ator Invisível</i>", escrito pelo ator e diretor <i>Yoshi Oida</i> da companhia de Peter Brook. O primeiro experimento foi o espetáculo/performance "<i>A Expectativa</i>" (2004). O intuito dos dois era realizar uma criação que mesclasse teatro, artes plásticas/instalação, música e poesia. Não tardou para que esse projeto se transformasse em um espetáculo de título "<i>Expectantes</i>" (2006). A premiada atriz gaúcha <i>Suzana Saldanha</i> fez a preparação de elenco. Os Invisíveis passaram a ter sua sede na Fundição Progresso. Cumpriram várias temporadas e o espetáculo consolidou a existência da companhia.</p>
                            <p>A terceira empreitada foi o espetáculo de título "<i>ou Hamuretsu</i>" (2009). Um "<i>divisor de águas</i>" para os Invisíveis. Temporada no espaço da Armazém Cia de Teatro (Fundição Progresso) e participação na Mostra <i>X Tudo Cultural</i> do Teatro Sesi, no Centro do Rio de Janeiro. Na ficha do espetáculo, nomes como o do diretor inglês <i>David Herman</i>, (preparação de análise ativa) e de <i>Yoshi Oida</i>, (ator  japonês da companhia de Peter Brook) em uma partipação exclusiva em vídeo fazendo o papel do fantasma de Hamlet. Presença de jurados de prêmios de teatro, críticos e principalmente uma bela resposta do público, marcaram esse trabalho.</p>
                        </div>

                        <div class="col-sm-12 col-lg-6">
                            <p>A Cia de Atores Invisíveis precisava crescer e adquiriu sede própria na Rua André Cavalcanti no bairro da Lapa, Centro do Rio de Janeiro, nesse mesmo ano.</p>   
                            <p>Em 2012, sem sede e passando por grandes reformulações, é montado o espetáculo "<i>Manifesto do Fim</i>" que fica em cartaz por dois mêses na Fundição Progresso. </p>
                            <p>O ano de 2014 continua sendo de mudanças. O experimento "<i>Un Valser per Luna</i>" é montado durante a estada do diretor e ator Márcio Moreira na Europa. Uma linda apresentação é feita na cidade de Monsano, região de Marche na Itália.</p>
                            <p>De volta ao Brasil, Márcio monta "<i>Dr. Angust</i>", em 2015. O espetáculo cumpre apresentações em alguns espaços da cidade do Rio.</p>
                            <p>Em 2017 um novo espetáculo foi apresentado: "<i>Uma Valsa pra Lua</b></i>", o primeiro trabalho para as crianças! </p>
                            <p>Entre toda luz e toda sombra, que fazem parte de qualquer história que pretende ser longa, com todas as certezas e incertezas do caminho... ainda assim, persiste a necessidade de contar novas histórias. </p>                          
                        </div>

